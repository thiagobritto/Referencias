/**
 * a palavra const declara uma constante (uma variavel js com valor constamte)
 * as constantes são semelhantes as variaveis let, exceto que o valor não pode ser alterado
 */

const x = 10
// aqui x é 10

console.log(x);

{
  const x = 2
  // aqui x é 2

  console.log(x);
}

// aqui x é 10
console.log(x);